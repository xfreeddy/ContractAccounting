def add_bug(title, steps, expected, actual, cause, fix):
    h = doc.add_paragraph()
    rh = h.add_run(title)
    rh.bold = True
    rh.font.size = Pt(14)
    set_spacing(h, after=4)

    sp = doc.add_paragraph()
    sp.add_run("Шаги воспроизведения:").bold = True
    set_spacing(sp, after=2)
    for s in steps:
        doc.add_paragraph(s, style="List Number")

    for label, val in [
        ("Ожидаемый результат: ", expected),
        ("Фактический результат (до исправления): ", actual),
        ("Причина: ", cause),
        ("Исправление: ", fix),
    ]:
        p = doc.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
        p.add_run(label).bold = True
        p.add_run(val)
        set_spacing(p, after=4)
    doc.add_paragraph()


