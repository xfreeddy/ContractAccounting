GRAY = RGBColor(0x80, 0x80, 0x80)


def set_spacing(p, after=6, line=1.5):
    pf = p.paragraph_format
    pf.space_after = Pt(after)
    pf.line_spacing = line


def add_center(text, size=14, bold=False):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = p.add_run(text)
    r.bold = bold
    r.font.size = Pt(size)
    r.font.name = "Times New Roman"
    set_spacing(p, after=0, line=1.0)
    return p


def add_para(text, justify=True, after=6):
    p = doc.add_paragraph(text)
    if justify:
        p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    set_spacing(p, after=after)
    return p


def add_figure_caption(text):
    """Плейсхолдер места под рисунок + подпись по центру."""
    ph = doc.add_paragraph()
    ph.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = ph.add_run("[ вставьте скриншот ]")
    r.italic = True
    r.font.color.rgb = GRAY
    r.font.size = Pt(12)
    set_spacing(ph, after=2, line=1.0)

    cap = doc.add_paragraph()
    cap.alignment = WD_ALIGN_PARAGRAPH.CENTER
    rc = cap.add_run(text)
    rc.font.size = Pt(12)
    set_spacing(cap, after=10, line=1.0)


