-- ========================================
-- Создание базы данных "УчётДоговоров2"
-- ========================================
IF NOT EXISTS (SELECT name FROM sys.databases WHERE name = N'УчётДоговоров2')
BEGIN
    CREATE DATABASE УчётДоговоров2;
END
GO

USE УчётДоговоров2;
GO

-- ========================================
-- Таблица: Пользователи
-- ========================================
IF NOT EXISTS (SELECT * FROM sysobjects WHERE name='Пользователи' AND xtype='U')
CREATE TABLE Пользователи (
    КодПользователя INT PRIMARY KEY IDENTITY(1,1),
    ИмяПользователя VARCHAR(50) UNIQUE NOT NULL,
    ХешПароля VARCHAR(255) NOT NULL,
    Роль VARCHAR(20) NOT NULL CHECK (Роль IN ('Администратор', 'Менеджер'))
);
GO

-- ========================================
-- Таблица: Контрагенты
-- ========================================
IF NOT EXISTS (SELECT * FROM sysobjects WHERE name='Контрагенты' AND xtype='U')
CREATE TABLE Контрагенты (
    КодКонтрагента INT PRIMARY KEY IDENTITY(1,1),
    Наименование NVARCHAR(255) NOT NULL,
    ИНН VARCHAR(12) UNIQUE NOT NULL,
    КонтактныйТелефон VARCHAR(20)
);
GO

-- ========================================
-- Таблица: Договоры
-- ========================================
IF NOT EXISTS (SELECT * FROM sysobjects WHERE name='Договоры' AND xtype='U')
CREATE TABLE Договоры (
    КодДоговора INT PRIMARY KEY IDENTITY(1,1),
    КодКонтрагента INT NOT NULL,
    КодПользователя INT NULL,
    Наименование NVARCHAR(255) NOT NULL,
    ДатаНачала DATE NOT NULL,
    ДатаОкончания DATE NOT NULL,
    Сумма DECIMAL(15,2) NOT NULL CHECK (Сумма > 0),
    ТипДоговора NVARCHAR(50) NOT NULL,
    Статус VARCHAR(20) DEFAULT 'Активен',
    ПутьКФайлу NVARCHAR(500)
);
GO

-- ========================================
-- Внешние ключи
-- ========================================
IF NOT EXISTS (SELECT * FROM sys.foreign_keys WHERE name='FK_Договоры_Контрагенты')
ALTER TABLE Договоры
ADD CONSTRAINT FK_Договоры_Контрагенты
FOREIGN KEY (КодКонтрагента) 
REFERENCES Контрагенты(КодКонтрагента);
GO

IF NOT EXISTS (SELECT * FROM sys.foreign_keys WHERE name='FK_Договоры_Пользователи')
ALTER TABLE Договоры
ADD CONSTRAINT FK_Договоры_Пользователи
FOREIGN KEY (КодПользователя) 
REFERENCES Пользователи(КодПользователя);
GO

-- ========================================
-- Индексы
-- ========================================
IF NOT EXISTS (SELECT * FROM sys.indexes WHERE name='IX_Договоры_ДатаОкончания')
CREATE INDEX IX_Договоры_ДатаОкончания ON Договоры(ДатаОкончания);
GO

IF NOT EXISTS (SELECT * FROM sys.indexes WHERE name='IX_Контрагенты_ИНН')
CREATE INDEX IX_Контрагенты_ИНН ON Контрагенты(ИНН);
GO

-- ========================================
-- Триггер: Автоматическое обновление статуса
-- ========================================
IF EXISTS (SELECT * FROM sys.triggers WHERE name='trg_ОбновитьСтатусДоговора')
DROP TRIGGER trg_ОбновитьСтатусДоговора;
GO

CREATE TRIGGER trg_ОбновитьСтатусДоговора
ON Договоры
AFTER INSERT, UPDATE
AS
BEGIN
    UPDATE d
    SET Статус = 'Истёк'
    FROM Договоры d
    INNER JOIN inserted i ON d.КодДоговора = i.КодДоговора
    WHERE i.ДатаОкончания < CAST(GETDATE() AS DATE)
END;
GO

